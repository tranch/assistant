func Avatar(s string) ([]byte, error) {
	r := []rune(s)
	if len(r) < 1 {
		return nil, errors.New("error avatar string")
	}
	s = strings.ToUpper(string(r[0]))

	rr := rand.New(rand.NewSource(time.Now().UnixNano()))

	font, err := truetype.Parse(goregular.TTF)
	if err != nil {
		return nil, err
	}
	face := truetype.NewFace(font, &truetype.Options{Size: 200})

	dc := gg.NewContext(200, 200)
	dc.SetFontFace(face)
	dc.SetColor(palette[rr.Intn(len(palette))])
	dc.Clear()
	dc.SetRGB(1, 1, 1)
	dc.DrawString(s, 30, 170)

	buf := new(bytes.Buffer)
	err = png.Encode(buf, dc.Image())
	if err != nil {
		return nil, err
	}

	return buf.Bytes(), nil
}

